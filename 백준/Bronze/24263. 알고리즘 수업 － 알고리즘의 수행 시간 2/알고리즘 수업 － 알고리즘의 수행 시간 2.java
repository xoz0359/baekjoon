
import java.util.*;

public class Main {

	public static void main(String[] args) {
		// 백준의 시간복잡도 2번 문제
		// 함수 안에 parameter n을 제어변수로 사용하는 반복문이 하나 있음으로
		// 이 코드의 실행 횟수는 pow(n, 1)번이고 최대 차항은 1이다
		Scanner input = new Scanner(System.in);
		int a = input.nextInt();
		System.out.println(a);
		System.out.println(1);	
	}
}